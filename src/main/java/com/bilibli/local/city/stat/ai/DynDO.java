package com.bilibli.local.city.stat.ai;

public class DynDO {

    public int id;

    public long dynamic_id;

    public int rid;

    public double ai_score;

    public int heat;

    public int heat_rank;

    public int ai_rank;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public long getDynamic_id() {
        return dynamic_id;
    }

    public void setDynamic_id(long dynamic_id) {
        this.dynamic_id = dynamic_id;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public double getAi_score() {
        return ai_score;
    }

    public void setAi_score(double ai_score) {
        this.ai_score = ai_score;
    }

    public int getHeat() {
        return heat;
    }

    public void setHeat(int heat) {
        this.heat = heat;
    }

    public int getHeat_rank() {
        return heat_rank;
    }

    public void setHeat_rank(int heat_rank) {
        this.heat_rank = heat_rank;
    }

    public int getAi_rank() {
        return ai_rank;
    }

    public void setAi_rank(int ai_rank) {
        this.ai_rank = ai_rank;
    }
}
